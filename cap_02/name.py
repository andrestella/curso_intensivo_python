name = "ADA lovelace"
print(name)
print(name.title()) # ".title()": exibe a primeira letra de cada palavra dentro da string com letra maiúscula
print(name.upper()) # ".upper()": muda uma string para que tenha somente letras maiúsculas
print(name.lower()) # ".lower()": muda uma string para que tenha somente letras minúsculas