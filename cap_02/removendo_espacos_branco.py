nome = "\tAndré Stella\n"
print(nome)
print(nome.lstrip()) # ".lstrip()": remove espaços em branco do lado esquerdo de uma string
print(nome.rstrip()) # ".rstrip()": remove espaços em branco do lado direito de uma string
print(nome.strip()) # ".strip()": remove espaços em branco de ambos os lados de uma string